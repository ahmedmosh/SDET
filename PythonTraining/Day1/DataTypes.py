# data types help us to know the class of the inputed data
# we are in putting in a variable and this can be gotten
# with the "type" keyword because python is a dynamically typed
# programming language.


x = 100
y = 12.10
s = 'welcome'
c = "A"
b = True

print(type(x)) # this should print interger class
print(type(y)) # this should print float class
print(type(s)) # this should print string class
print(type(c)) # this should print string class
print(type(b)) # this should print boolean class
