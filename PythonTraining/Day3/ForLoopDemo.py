# For loop
# To print 1 - 10 number using "for loop" and "in" function

for i in range(11):
    print(i)
# To print only even number 1 - 20

for x in range(0, 21, 2):  # 0-starting number 21-range 2-increment
    print(x)

# To print 1 - 10 in descending order
for z in range(10, 0, -1):
    print(z)
