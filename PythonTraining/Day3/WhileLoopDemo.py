# Loop is basically a block of statement which can be repeated
# multiple times.


# _______while loop ______
# print 1 to 10 numbers
# steps include:
# initialisation
# condition
# incrementation

i = 1  # initialization
while i <= 10:  # condition
    print(i)
    i = i + 1  # incrementation

# To print 1 - 10 numbers in descending order

x = 10
while x >= 1:
    print(x)
    x = x - 1
